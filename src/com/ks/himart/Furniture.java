package com.ks.himart;

public class Furniture {

	private int price;
	private int point;
	
	public int getPrice() {
		return price;
	}


	public int setPrice(int price) {
		this.price = price;
		return price;
	}


	public int getPoint() {
		return point;
	}


	public int setPoint(int point) {
		this.point = point;
		return point;
	}
	
	
	
}
